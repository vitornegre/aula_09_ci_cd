def area_of_rectangle(width, height):
    area = width*height
    return area
 
def perimeter_of_rectangle(width, height):
    perimeter = 2 * (width + height)
    return perimeter

def sum(num1, num2):
    sum = num1 + num2
    return sum

def difference(num1, num2):
    difference = num1 - num2
    return difference

def product(num1, num2):
    product = num1 * num2
    return product

def division(num1, num2):
    division = num1 / num2
    return division